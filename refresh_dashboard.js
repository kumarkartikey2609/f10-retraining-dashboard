// ── Redash API helpers ───────────────────────────────────────
function apiRequest(method, urlPath, body) {
  return new Promise((resolve, reject) => {
    const data = body ? JSON.stringify(body) : null;
    const options = {
      hostname: REDASH_URL,
      path: urlPath,
      method: method,
      rejectUnauthorized: false,
      headers: {
        'Authorization': `Key ${API_KEY}`,
        'Content-Type': 'application/json',
        ...(data ? { 'Content-Length': Buffer.byteLength(data) } : {})
      }
    };
    const req = https.request(options, res => {
      let raw = '';
      res.on('data', chunk => raw += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(raw)); }
        catch(e) { reject(new Error('Bad JSON: ' + raw.slice(0, 200))); }
      });
    });
    req.on('error', (e) => { console.error('   Network error:', e.message); reject(e); });
    req.setTimeout(30000, () => { req.destroy(); reject(new Error('Timeout')); });
    if (data) req.write(data);
    req.end();
  });
}

function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

async function triggerRefresh() {
  try {
    const res = await apiRequest('POST', `/api/queries/${QUERY_ID}/results`, { parameters: {} });
    return res.job && res.job.id;
  } catch(e) { return null; }
}

async function pollJob(jobId, timeoutMs = 600000) {
  process.stdout.write('   Waiting for query to finish (up to 10 min for full-year data)');
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    try {
      const res = await apiRequest('GET', `/api/jobs/${jobId}`);
      const job = res.job || {};
      if (job.status === 3) { console.log(' done ✅'); return job.query_result_id; }
      if (job.status === 4) { console.log(` failed ❌ (error: ${job.error})`); return null; }
      process.stdout.write('.');
      await sleep(5000);
    } catch(e) { await sleep(5000); }
  }
  console.log(' timed out ⏱ — will try cached results anyway');
  return null;
}

async function getResultById(resultId) {
  try {
    const res = await apiRequest('GET', `/api/query_results/${resultId}`);
    const qr  = res.query_result || {};
    const rows = (qr.data || {}).rows || [];
    console.log(`   Fetched ${rows.length} rows from result #${resultId}`);
    return { rows, retrieved_at: qr.retrieved_at || '' };
  } catch(e) { return null; }
}

