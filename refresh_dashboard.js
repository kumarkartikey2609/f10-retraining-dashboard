// ── Local HTTP server ────────────────────────────────────────
const MIME = {
  '.html': 'text/html',
  '.js':   'application/javascript',
  '.css':  'text/css',
  '.json': 'application/json',
  '.png':  'image/png',
  '.ico':  'image/x-icon',
};

function startServer() {
  return new Promise((resolve, reject) => {
    const server = http.createServer((req, res) => {
      const cors = {
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
        'Access-Control-Allow-Headers': 'Content-Type'
      };

      // Save Apps Script URL to trainer_config.js
      if (req.method === 'POST' && req.url === '/save-config') {
        let body = '';
        req.on('data', c => body += c);
        req.on('end', () => {
          try {
            const { url } = JSON.parse(body);
            const configPath = path.join(DIR, 'trainer_config.js');
            fs.writeFileSync(configPath,
              `// Auto-saved by F10 Dashboard — do not edit manually\nvar TRAINER_APPS_SCRIPT_URL = ${JSON.stringify(url)};\n`,
              'utf8');
            console.log('   ✅ Apps Script URL saved to trainer_config.js');
            res.writeHead(200, { 'Content-Type': 'application/json', ...cors });
            res.end(JSON.stringify({ ok: true }));
          } catch(e) {
            res.writeHead(500, { 'Content-Type': 'application/json', ...cors });
            res.end(JSON.stringify({ error: e.message }));
          }
        });
        return;
      }

      if (req.method === 'OPTIONS') {
        res.writeHead(204, cors); res.end(); return;
      }

      // Serve files from the dashboard directory
      let filePath = path.join(DIR, req.url === '/' ? DASHBOARD : req.url.split('?')[0]);
      // Security: only serve files within DIR
      if (!filePath.startsWith(DIR)) { res.writeHead(403); res.end(); return; }
      fs.readFile(filePath, (err, data) => {
        if (err) {
          res.writeHead(404, {'Content-Type': 'text/plain'});
          res.end('Not found');
          return;
        }
        const ext = path.extname(filePath).toLowerCase();
        const ct  = MIME[ext] || 'application/octet-stream';
        res.writeHead(200, {'Content-Type': ct, ...cors});
        res.end(data);
      });
    });
    server.listen(PORT, '127.0.0.1', () => {
      console.log(`   🌐 Local server running at http://localhost:${PORT}`);
      resolve(server);
    });
    server.on('error', e => {
      if (e.code === 'EADDRINUSE') {
        console.log(`   ⚠️  Port ${PORT} in use — dashboard may already be open`);
        resolve(null);
      } else reject(e);
    });
  });
}

// ── CSV → trainer_data.js ────────────────────────────────────
function parseCsv(text) {
  const lines = text.split(/\r?\n/).filter(l => l.trim());
  if (lines.length < 2) return [];
  function splitLine(line) {
    const fields = []; let cur = '', inQ = false;
    for (let i = 0; i < line.length; i++) {
      const ch = line[i];
      if (ch === '"') { if (inQ && line[i+1] === '"') { cur += '"'; i++; } else inQ = !inQ; }
      else if (ch === ',' && !inQ) { fields.push(cur); cur = ''; }
      else cur += ch;
    }
    fields.push(cur); return fields;
  }
  const headers = splitLine(lines[0]).map(h => h.trim());
  return lines.slice(1).map(line => {
    const vals = splitLine(line);
    const obj = {};
    headers.forEach((h, i) => { obj[h] = (vals[i] || '').trim(); });
    return obj;
  }).filter(row => Object.values(row).some(v => v));
}

function processTrainerCsv() {
  // Accept trainer_sheet.csv or trainer_sheet.csv.csv (Windows double-extension)
  const csvPath = [TRAINER_CSV, TRAINER_CSV + '.csv'].find(p => fs.existsSync(p));
  if (!csvPath) return false;
  const TRAINER_CSV_FOUND = csvPath;
  try {
    const text = fs.readFileSync(TRAINER_CSV_FOUND, 'utf8');
    const rawRows = parseCsv(text);
    if (!rawRows.length) return false;

    // Normalize: pick the right column regardless of exact header name
    const get = (row, ...keys) => {
      for (const k of keys) {
        // exact match
        if (row[k] !== undefined && String(row[k]).trim() !== '') return String(row[k]).trim();
        // case-insensitive match
        const found = Object.keys(row).find(rk => rk.trim().toLowerCase() === k.toLowerCase());
        if (found && String(row[found]).trim() !== '') return String(row[found]).trim();
      }
      return '';
    };

    const rows = rawRows
      .filter(r => get(r, 'PROVIDER_ID'))
      .map(row => ({
        PROVIDER_ID:        get(row, 'PROVIDER_ID'),
        PROVIDER_NAME:      get(row, 'PROVIDER_NAME'),
        APPROVAL:           get(row, 'APPROVAL'),
        MONTH:              get(row, 'MONTH'),
        CURRENT_AVG_RATING: get(row, 'CURRENT_AVG_RATING'),
        FILLED_BY:          get(row, 'FILLED_BY'),
        FIRST_10_RATING:    get(row, 'FIRST_10_RATING'),
        RATED_COUNT:        get(row, 'RATED_COUNT'),
        TRAINER:            get(row, 'Trainer', 'TRAINER', 'Trainer::multi-filter', 'TRAINER_MULTI_FILTER'),
        F10_RESULT:         get(row, 'F10 Result', 'F10_RESULT', 'F10_Result'),
        MONTH:              get(row, 'MONTH', 'APPROVAL'),
        APPROVAL_MONTH:     get(row, 'MONTH', 'APPROVAL'),
      }));

    if (!rows.length) { console.log('   ⚠️  trainer_sheet.csv had no valid data rows'); return false; }

    const dateStr = new Date().toUTCString().replace(' GMT', ' UTC');
    const content = [
      '// Auto-generated from trainer_sheet.csv — do not edit manually',
      `// Last updated: ${dateStr}`,
      `var TRAINER_RAW = ${JSON.stringify(rows)};`,
      `var TRAINER_DATE = "${dateStr}";`,
      ''
    ].join('\n');
    fs.writeFileSync(TRAINER_JS, content, 'utf8');
    console.log(`   ✅ trainer_data.js built from CSV  |  ${rows.length} rows`);
    return true;
  } catch(e) {
    console.log('   ⚠️  Could not process trainer_sheet.csv:', e.message);
    return false;
  }
}

